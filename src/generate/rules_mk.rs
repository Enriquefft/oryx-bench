//! Emit `rules.mk` additions from `features.toml [features]` and the
//! contents of `overlay/`.
//!
//! Walks `overlay/*.c` (Tier 2′ vendored C) and appends each to
//! `SRC +=` so the QMK build picks them up.
//!
//! Tier 2 Zig support is not implemented in v0.1. When we add it, the
//! design will be: pre-compile each `overlay/*.zig` into a `.o` via
//! `zig cc` in the docker backend, then emit a corresponding
//! `EXTRAOBJS += path/to/overlay.o` line here. Until then the Zig path
//! is documented in ARCHITECTURE.md as future work and deliberately
//! not wired up — the process-record-user-collision lint still scans
//! `.zig` files so authored Zig code surfaces in static analysis.

use std::fmt::Write;
use std::path::Path;

use anyhow::{Context, Result};
use walkdir::WalkDir;

use crate::schema::features::FeaturesToml;

/// Emit the additive `rules.mk` body.
pub fn emit_rules_mk(
    features: &FeaturesToml,
    overlay_dir: Option<&Path>,
    has_tap_dances: bool,
) -> Result<String> {
    let mut out = String::new();
    out.push_str("# SPDX-License-Identifier: MIT\n");
    out.push_str("# Generated by oryx-bench. Do not edit by hand.\n\n");

    // Translate [features] toggles into QMK feature flags.
    out.push_str("# Feature flags from overlay/features.toml [features]\n");
    let mut keys: Vec<&String> = features.features.keys().collect();
    keys.sort();
    for key in keys {
        let yes = features.features[key];
        let flag_name = match key.as_str() {
            "key_overrides" => "KEY_OVERRIDE_ENABLE",
            "combos" => "COMBO_ENABLE",
            "caps_word" => "CAPS_WORD_ENABLE",
            "mouse_keys" => "MOUSEKEY_ENABLE",
            "tap_dance" => "TAP_DANCE_ENABLE",
            "rgb_matrix" => "RGB_MATRIX_ENABLE",
            other => {
                // Fallback: uppercase + _ENABLE
                let _ = writeln!(
                    out,
                    "{}_ENABLE = {}",
                    other.to_uppercase(),
                    if yes { "yes" } else { "no" }
                );
                continue;
            }
        };
        let _ = writeln!(out, "{flag_name} = {}", if yes { "yes" } else { "no" });
    }
    out.push('\n');

    // Auto-enable tap dance when the layout uses double_tap keys,
    // regardless of whether the user declared it in [features].
    if has_tap_dances && !features.features.contains_key("tap_dance") {
        out.push_str("# Auto-enabled: layout uses tap-dance keys\n");
        out.push_str("TAP_DANCE_ENABLE = yes\n\n");
    }

    // Disable the DFU suffix. The Voyager's upstream rules.mk sets
    // DFU_SUFFIX_ARGS which stamps vendor/product IDs into the .bin.
    // oryx-bench flashes via wally-cli or keymapp, neither of which
    // needs the suffix. wally-cli v2.0.x has a byte-order bug that
    // rejects the suffix produced by dfu-util >= 0.11. Rather than
    // working around the bug, we remove the root cause: the suffix
    // has no purpose in this pipeline.
    out.push_str("# Disable DFU suffix — not needed for wally-cli/keymapp flashing\n");
    out.push_str("DFU_SUFFIX_ARGS =\n\n");

    // Always-on additions: the generated _features.c.
    out.push_str("SRC += _features.c\n");

    // Walk overlay/ for .c files. Walkdir errors propagate so we
    // don't silently miss a file the user is expecting to compile.
    if let Some(dir) = overlay_dir {
        if dir.exists() {
            let mut c_files: Vec<String> = Vec::new();
            for entry in WalkDir::new(dir) {
                let entry = entry
                    .with_context(|| format!("walking overlay directory {}", dir.display()))?;
                if let Some(ext) = entry.path().extension().and_then(|s| s.to_str()) {
                    if ext == "c" {
                        let rel = entry.path().strip_prefix(dir).with_context(|| {
                            format!("stripping overlay prefix from {}", entry.path().display())
                        })?;
                        c_files.push(rel.display().to_string());
                    }
                }
            }
            c_files.sort();
            if !c_files.is_empty() {
                out.push_str("\n# Tier 2′ vendored C files (overlay/*.c)\n");
                for f in c_files {
                    let _ = writeln!(out, "SRC += {f}");
                }
            }
        }
    }

    Ok(out)
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::collections::BTreeMap;
    use tempfile::TempDir;

    #[test]
    fn emit_rules_mk_translates_feature_flags() {
        let mut feats = BTreeMap::new();
        feats.insert("key_overrides".to_string(), true);
        feats.insert("combos".to_string(), false);
        feats.insert("caps_word".to_string(), true);
        let features = FeaturesToml {
            features: feats,
            ..Default::default()
        };
        let out = emit_rules_mk(&features, None, false).unwrap();
        assert!(out.contains("KEY_OVERRIDE_ENABLE = yes"));
        assert!(out.contains("COMBO_ENABLE = no"));
        assert!(out.contains("CAPS_WORD_ENABLE = yes"));
    }

    #[test]
    fn emit_rules_mk_walks_overlay_for_c_files() {
        let td = TempDir::new().unwrap();
        std::fs::write(td.path().join("foo.c"), "// pasted upstream").unwrap();
        std::fs::write(td.path().join("bar.c"), "// pasted upstream").unwrap();
        std::fs::write(td.path().join("notes.md"), "# ignored").unwrap();
        let out = emit_rules_mk(&FeaturesToml::default(), Some(td.path()), false).unwrap();
        assert!(out.contains("SRC += bar.c"));
        assert!(out.contains("SRC += foo.c"));
        assert!(!out.contains("notes.md"));
    }

    #[test]
    fn always_includes_generated_features_c() {
        let out = emit_rules_mk(&FeaturesToml::default(), None, false).unwrap();
        assert!(out.contains("SRC += _features.c"));
    }

    #[test]
    fn emit_rules_mk_auto_enables_tap_dance() {
        let out = emit_rules_mk(&FeaturesToml::default(), None, true).unwrap();
        assert!(out.contains("TAP_DANCE_ENABLE = yes"));
        assert!(out.contains("Auto-enabled"));
    }

    #[test]
    fn emit_rules_mk_no_auto_enable_when_no_tap_dances() {
        let out = emit_rules_mk(&FeaturesToml::default(), None, false).unwrap();
        assert!(!out.contains("TAP_DANCE_ENABLE"));
    }
}

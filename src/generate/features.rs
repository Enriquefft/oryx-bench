/// Emit `combo_t key_combos[]` from `[[combos]]`. Position names are
/// resolved to keycodes via the visual layout — each combo lists the
/// keycodes that must be pressed simultaneously.
///
/// Returns an error if any combo references an unknown layer, an
/// unknown position, an unknown geometry, or a position with an
/// unbound key. The previous behavior of writing a `// skipped`
/// comment into the C source silently dropped combos at build time;
/// failing the build is the only way to surface the bug.
pub fn emit_combos(features: &FeaturesToml, layout: &CanonicalLayout) -> Result<String> {
    let mut out = String::from("// Combos\n");

    let geom = crate::schema::geometry::get(layout.geometry.as_str()).ok_or_else(|| {
        anyhow!(
            "combos defined in features.toml but layout uses unknown geometry '{}'",
            layout.geometry
        )
    })?;

    for (i, combo) in features.combos.iter().enumerate() {
        // Resolve each position name to the keycode bound at that position
        // on the relevant layer (default: layer 0).
        let layer_name = combo.layer.as_deref().unwrap_or("Main");
        let layer = layout
            .layers
            .iter()
            .find(|l| l.name == layer_name)
            .or_else(|| layout.layers.iter().find(|l| l.position == 0))
            .ok_or_else(|| {
                anyhow!("combo #{i} references layer '{layer_name}' which does not exist")
            })?;
        let mut codes: Vec<String> = Vec::with_capacity(combo.keys.len());
        for pos in &combo.keys {
            let idx = geom.position_to_index(pos).ok_or_else(|| {
                anyhow!(
                    "combo #{i} references position '{pos}' which is not valid for geometry '{}'",
                    layout.geometry
                )
            })?;
            let key = &layer.keys[idx];
            let kc = key
                .tap
                .as_ref()
                .and_then(|a| a.tap_keycode())
                .ok_or_else(|| {
                    anyhow!(
                        "combo #{i} references position '{pos}' on layer '{layer_name}' which has no bound keycode (combos must reference real keycodes)"
                    )
                })?;
            codes.push(kc.canonical_name().into_owned());
        }
        let _ = writeln!(
            out,
            "static const uint16_t PROGMEM combo_{i}[] = {{ {}, COMBO_END }};",
            codes.join(", ")
        );
    }

    out.push_str("combo_t key_combos[] = {\n");
    for (i, combo) in features.combos.iter().enumerate() {
        let sends = normalize_keycode_token(&combo.sends);
        let _ = writeln!(out, "    [{i}] = COMBO(combo_{i}, {sends}),");
    }
    out.push_str("};\n");

    Ok(out)
}


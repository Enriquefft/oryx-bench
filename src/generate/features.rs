/// Default achordion timeout in milliseconds when no per-key override
/// is set. Matches the upstream achordion library default.
const ACHORDION_DEFAULT_TIMEOUT_MS: u32 = 800;

/// Translate a features.toml binding string (e.g. `"LT(Sym+Num, BSPC)"`)
/// into a valid C QMK token (e.g. `"LT(SYM_NUM, KC_BSPC)"`) by parsing
/// it through `schema::layout::parse_action` and re-emitting through
/// `keymap::emit_action`. Used by achordion + tapping_term emitters so
/// the user can write symbolic layer + bare keycode names.
///
/// Returns an error if the binding references a layer that doesn't
/// exist in the visual layout — the caller wraps the error with the
/// original binding string so the user can find the offending entry.
fn translate_binding(s: &str, layers: &LayerTable) -> Result<String> {
    let action = parse_action(s);
    emit_action(&action, layers, &CustomKeycodeTable::new())
        .with_context(|| format!("translating features.toml binding '{s}'"))
}

/// Emit `_features.h` — declarations both `keymap.c` and `_features.c`
/// reference, including the custom-keycode enum so the two translation
/// units agree on symbol IDs.
pub fn emit_features_h(
    custom_keycodes: &CustomKeycodeTable,
    tap_dances: &TapDanceTable,
    layers: &LayerTable,
) -> String {
    let mut out = String::new();
    out.push_str("// SPDX-License-Identifier: MIT\n");
    out.push_str("// Generated by oryx-bench. Do not edit by hand.\n");
    out.push_str("#pragma once\n");
    out.push_str("#include QMK_KEYBOARD_H\n\n");

    // Layer enum -- shared between keymap.c and _features.c so both
    // translation units can reference layer names (e.g. in LT(), TO(),
    // achordion_timeout, combo layer filters).
    if !layers.is_empty() {
        out.push_str("enum layers {\n");
        let mut sorted: Vec<_> = layers.iter().collect();
        sorted.sort_by_key(|(_, v)| v.position);
        for (_, entry) in &sorted {
            let _ = writeln!(out, "    {},", entry.ident);
        }
        out.push_str("};\n\n");
    }

    if !custom_keycodes.is_empty() {
        out.push_str("enum custom_keycodes {\n");
        let mut first = true;
        for entry in custom_keycodes.values() {
            if first {
                let _ = writeln!(out, "    {} = SAFE_RANGE,", entry.ident);
                first = false;
            } else {
                let _ = writeln!(out, "    {},", entry.ident);
            }
        }
        out.push_str("};\n\n");
    }

    if !tap_dances.is_empty() {
        out.push_str("// Tap dance indices\n");
        out.push_str("enum tap_dance_ids {\n");
        for td in tap_dances {
            let _ = writeln!(out, "    TD_{},", td.td_index);
        }
        out.push_str("};\n\n");
        out.push_str("extern tap_dance_action_t tap_dance_actions[];\n\n");
    }

    out.push_str(
        "// Tier 2 (.zig/.c) overlay code may implement this hook to extend\n\
         // the generated process_record_user dispatch. Default is a no-op.\n",
    );
    out.push_str("bool process_record_user_overlay(uint16_t keycode, keyrecord_t *record);\n");
    out
}

/// Emit the full `_features.c` body.
///
/// Returns an error when codegen would silently produce broken C —
/// e.g. a combo binding that references an unknown position.
pub fn emit_features_c(
    features: &FeaturesToml,
    layers: &LayerTable,
    custom_keycodes: &CustomKeycodeTable,
    _layout: &CanonicalLayout,
    tap_dances: &TapDanceTable,
) -> Result<String> {
    let mut out = String::new();
    out.push_str("// SPDX-License-Identifier: MIT\n");
    out.push_str("// Generated by oryx-bench. Do not edit by hand.\n");
    out.push_str("#include QMK_KEYBOARD_H\n");
    out.push_str("#include \"_features.h\"\n");
    if features
        .achordion
        .as_ref()
        .map(|a| a.enabled)
        .unwrap_or(false)
    {
        // Suppress deprecation #warning in legacy achordion.h — QMK
        // compiles with -Werror. Module-based replacement not in ZSA fork yet.
        out.push_str("#pragma GCC diagnostic push\n");
        out.push_str("#pragma GCC diagnostic ignored \"-Wcpp\"\n");
        out.push_str("#include \"achordion.h\"\n");
        out.push_str("#pragma GCC diagnostic pop\n");
    }
    out.push('\n');

    if !tap_dances.is_empty() {
        out.push_str(&emit_tap_dance_actions(
            tap_dances,
            layers,
            custom_keycodes,
        )?);
        out.push('\n');
    }

    if !features.key_overrides.is_empty() {
        out.push_str(&emit_key_overrides(features)?);
        out.push('\n');
    }

    if !features.tapping_term_per_key.is_empty() {
        out.push_str(&emit_tapping_term_per_key(features, layers)?);
        out.push('\n');
    }

    // NOTE: combos are emitted in keymap.c (not here) because QMK's
    // keymap_introspection.c includes keymap.c via `#include KEYMAP_C`
    // and expects `combo_t key_combos[]` to be visible there.

    if let Some(achordion) = &features.achordion {
        if achordion.enabled {
            out.push_str(&emit_achordion(features, layers)?);
            out.push('\n');
        }
    }

    out.push_str(&emit_process_record_user(custom_keycodes, features));
    out.push('\n');

    Ok(out)
}

fn emit_key_overrides(features: &FeaturesToml) -> Result<String> {
    let mut out = String::from("// Key overrides\n");
    for (i, ko) in features.key_overrides.iter().enumerate() {
        let _ = writeln!(
            out,
            "static const key_override_t ko_{i} = ko_make_basic({}, {}, {});",
            mods_to_qmk(&ko.mods)?,
            normalize_keycode_token(&ko.key),
            normalize_keycode_token(&ko.sends)
        );
    }
    out.push_str("const key_override_t **key_overrides = (const key_override_t *[]){\n");
    for (i, _) in features.key_overrides.iter().enumerate() {
        let _ = writeln!(out, "    &ko_{i},");
    }
    out.push_str("    NULL\n};\n");
    Ok(out)
}

fn mods_to_qmk(mods: &[String]) -> Result<String> {
    if mods.is_empty() {
        return Ok("0".to_string());
    }
    let mut parts = Vec::with_capacity(mods.len());
    for raw in mods {
        let bare = raw.trim_start_matches("KC_").trim_start_matches("MOD_");
        if !is_valid_modifier_token(bare) {
            return Err(anyhow!(
                "key_override mods entry '{raw}' is not a recognized QMK modifier"
            ));
        }
        parts.push(format!("MOD_{bare}"));
    }
    Ok(parts.join(" | "))
}

fn is_valid_modifier_token(t: &str) -> bool {
    matches!(
        t,
        "LCTL"
            | "LSFT"
            | "LALT"
            | "LGUI"
            | "RCTL"
            | "RSFT"
            | "RALT"
            | "RGUI"
            | "LSHIFT"
            | "RSHIFT"
            | "LCTRL"
            | "RCTRL"
            | "HYPR"
            | "MEH"
    )
}

/// Normalize a keycode token written by the user in `features.toml`
/// into a QMK-compatible C token. Handles bare names (`BSPC` →
/// `KC_BSPC`), already-prefixed names (`KC_BSPC` → `KC_BSPC`), and
/// modifier wrappers like `S(GRAVE)` (recursing into the parens to
/// rewrite the inner token as `S(KC_GRAVE)`).
fn normalize_keycode_token(s: &str) -> String {
    let trimmed = s.trim();
    if let Some(open) = trimmed.find('(') {
        if let Some(close) = trimmed.rfind(')') {
            if close > open {
                let head = &trimmed[..open];
                let inner = &trimmed[open + 1..close];
                let rest = &trimmed[close + 1..];
                // Recursively normalize the inner token; supports
                // arbitrary nesting like `LCTL(LSFT(GRAVE))`.
                return format!("{head}({}){rest}", normalize_keycode_token(inner));
            }
        }
    }
    if trimmed.starts_with("KC_") {
        return trimmed.to_string();
    }
    // Bare name. Reject if it's not actually a known QMK keycode after
    // adding the prefix — that catches typos like `BACKSPC` early.
    let candidate = format!("KC_{trimmed}");
    if matches!(Keycode::from_str(&candidate), Keycode::Other(_)) {
        // Still emit the prefixed form so QMK gets a recognizable
        // token; lint will catch unknown keycodes separately.
        return candidate;
    }
    candidate
}

fn emit_tapping_term_per_key(features: &FeaturesToml, layers: &LayerTable) -> Result<String> {
    let mut out =
        String::from("uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {\n");
    out.push_str("    switch (keycode) {\n");
    for entry in &features.tapping_term_per_key {
        let translated = translate_binding(&entry.binding, layers)?;
        let _ = writeln!(out, "        case {translated}: return {};", entry.ms);
    }
    out.push_str("        default: return TAPPING_TERM;\n");
    out.push_str("    }\n");
    out.push_str("}\n");
    Ok(out)
}

fn emit_process_record_user(
    custom_keycodes: &CustomKeycodeTable,
    _features: &FeaturesToml,
) -> String {
    let mut out = String::new();
    out.push_str("// Forward declaration: Tier 2 (.zig/.c) overlay code may implement this.\n");
    out.push_str("__attribute__((weak)) bool process_record_user_overlay(uint16_t keycode, keyrecord_t *record) { return true; }\n\n");
    out.push_str("bool process_record_user(uint16_t keycode, keyrecord_t *record) {\n");
    out.push_str("    if (record->event.pressed) {\n");
    out.push_str("        switch (keycode) {\n");
    for entry in custom_keycodes.values() {
        let escaped = escape_c_string(&entry.body);
        let _ = writeln!(
            out,
            "            case {}: SEND_STRING(\"{escaped}\"); return false;",
            entry.ident
        );
    }
    out.push_str("            default: break;\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("    return process_record_user_overlay(keycode, record);\n");
    out.push_str("}\n");
    out
}

/// Escape a Rust string for embedding inside a C `"..."` string literal.
///
/// Handles every byte that would otherwise terminate the literal,
/// change its semantics, or be non-representable:
///
/// - `\`, `"`: the two characters that would break the literal syntax.
/// - `\n`, `\r`, `\t`: common whitespace; emitted as escapes so the
///   generated file stays on a single line.
/// - `\0`: would truncate the literal at compile time — `char s[] =
///   "foo\0bar"` stores `"foo"`. Emitted as `\0` so QMK's SEND_STRING
///   receives the exact byte we mean. (In practice users shouldn't
///   put nulls in macros, but the rule is "any input is safe to
///   pass to this function".)
/// - Other ASCII control characters (`<0x20` or `0x7F`): emitted in
///   `\xNN""` form. The trailing empty string is **load-bearing**:
///   C's `\x` escape is greedy and consumes any subsequent hex digits
///   without bound, so `"\x07A"` would parse as one character with
///   value `0x7A` (silently dropping the BEL we meant to escape).
///   Adjacent string literals are concatenated at translation phase
///   6, so `"\x07" "A"` collapses back to a 2-char string `BEL` + `A`
///   while terminating the hex run. See ISO C99 6.4.4.4 ¶7.
/// - Non-ASCII UTF-8 passes through unchanged: the user's source
///   already decided how to encode it and the C compiler treats
///   high-bit bytes as part of the string.
fn escape_c_string(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for ch in s.chars() {
        match ch {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            '\0' => out.push_str("\\0"),
            // Other ASCII control characters. The `""` after the hex
            // escape terminates C's greedy hex-digit run; see the
            // doc comment above for the rationale.
            c if (c as u32) < 0x20 || (c as u32) == 0x7F => {
                use std::fmt::Write as _;
                let _ = write!(out, "\\x{:02X}\"\"", c as u32);
            }
            c => out.push(c),
        }
    }
    out
}


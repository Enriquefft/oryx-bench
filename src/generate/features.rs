    #[test]
    fn normalize_keycode_token_handles_paren_wrappers() {
        // The fixture's `S(GRAVE)` key override must emit `S(KC_GRAVE)`,
        // not the broken `S(GRAVE)` that the old `with_kc_prefix` did.
        assert_eq!(normalize_keycode_token("BSPC"), "KC_BSPC");
        assert_eq!(normalize_keycode_token("KC_BSPC"), "KC_BSPC");
        assert_eq!(normalize_keycode_token("S(GRAVE)"), "S(KC_GRAVE)");
        assert_eq!(normalize_keycode_token("S(KC_GRAVE)"), "S(KC_GRAVE)");
        // Nested wrappers also recurse.
        assert_eq!(
            normalize_keycode_token("LCTL(LSFT(GRAVE))"),
            "LCTL(LSFT(KC_GRAVE))"
        );
    }


/// Emit achordion glue: callbacks for chord_strategy, per-key timeouts,
/// and no_streak overrides. Achordion's library body itself is **vendored**
/// upstream — the user must drop `achordion.c` and `achordion.h` into
/// `overlay/`. We document this in the cookbook.
fn emit_achordion(features: &FeaturesToml, layers: &LayerTable) -> Result<String> {
    let achordion = match &features.achordion {
        Some(a) if a.enabled => a,
        _ => return Ok(String::new()),
    };

    let mut out = String::from("// Achordion glue\n");
    out.push_str("// NOTE: requires `achordion.h` + `achordion.c` from\n");
    out.push_str("// https://getreuer.info/posts/keyboards/achordion/ to be vendored\n");
    out.push_str("// in overlay/.\n\n");

    // achordion_chord — implements chord_strategy.
    out.push_str("bool achordion_chord(uint16_t tap_hold_keycode, keyrecord_t* tap_hold_record,\n");
    out.push_str("                     uint16_t other_keycode, keyrecord_t* other_record) {\n");

    // Emit same_hand_allow exceptions first.
    for entry in &achordion.same_hand_allow {
        let tap_hold = translate_binding(&entry.tap_hold, layers)?;
        let other = translate_binding(&entry.other, layers)?;
        let _ = writeln!(
            out,
            "    if (tap_hold_keycode == {tap_hold} && other_keycode == {other}) return true;"
        );
    }

    match achordion.chord_strategy {
        ChordStrategy::Always => out.push_str("    return true;\n"),
        ChordStrategy::Never => out.push_str("    return false;\n"),
        ChordStrategy::OppositeHands => {
            out.push_str("    return achordion_opposite_hands(tap_hold_record, other_record);\n");
        }
    }
    out.push_str("}\n\n");

    // achordion_timeout — per-key timeout overrides.
    if !achordion.timeout.is_empty() {
        out.push_str("uint16_t achordion_timeout(uint16_t tap_hold_keycode) {\n");
        out.push_str("    switch (tap_hold_keycode) {\n");
        for entry in &achordion.timeout {
            let translated = translate_binding(&entry.binding, layers)?;
            let _ = writeln!(out, "        case {translated}: return {};", entry.ms);
        }
        let _ = writeln!(
            out,
            "        default: return {ACHORDION_DEFAULT_TIMEOUT_MS};"
        );
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }

    // achordion_streak_chord — disable streak chord disambiguation per-key.
    if !achordion.no_streak.is_empty() {
        out.push_str("bool achordion_streak_chord(uint16_t tap_hold_keycode) {\n");
        out.push_str("    switch (tap_hold_keycode) {\n");
        for entry in &achordion.no_streak {
            let translated = translate_binding(&entry.binding, layers)?;
            let _ = writeln!(out, "        case {translated}: return false;");
        }
        out.push_str("        default: return true;\n");
        out.push_str("    }\n");
        out.push_str("}\n");
    }

    Ok(out)
}


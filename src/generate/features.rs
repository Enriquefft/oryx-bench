//! Emit `_features.h` and `_features.c` from `overlay/features.toml`.
//!
//! `_features.h` declares the symbols (custom keycode enum,
//! `process_record_user_overlay` weak forward decl) that both
//! `keymap.c` and `_features.c` reference. `_features.c` defines them.
//!
//! Concerns covered (Tier 1 declarative features):
//!
//! - `enum custom_keycodes` declaration (header) + dispatch (source)
//! - `key_override_t` table from `[[key_overrides]]`
//! - `combos[]` array from `[[combos]]`
//! - `process_record_user` dispatch — handles macros, then chains to
//!   `process_record_user_overlay` if a Tier 2 file declares one
//! - `get_tapping_term` overrides from `[[tapping_term_per_key]]`
//! - Achordion glue (callbacks + per-key overrides) from `[achordion]`
//!
//! The generated `process_record_user` is the authority — Tier 2 hooks
//! call into `process_record_user_overlay` after we've handled our own
//! concerns. (See the `process-record-user-collision` lint rule.)

use std::fmt::Write;

use anyhow::{anyhow, Context, Result};

use crate::schema::canonical::CanonicalLayout;
use crate::schema::features::{ChordStrategy, FeaturesToml};
use crate::schema::keycode::Keycode;
